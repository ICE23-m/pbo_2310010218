    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {
        if (TxtID.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Silakan pilih data terlebih dahulu");
            jTable1.requestFocus();
        } else {
            crudObj.ubahLaporanPrepared(
                    TxtID.getText(),
                    TxtAlamat.getText(),
                    TxtWaktu.getText(),
                    TxtLap.getText(),
                    jTextField5.getText(),
                    TxtLak.getText(),
                    TxtJenis.getText(),
                    TxtLuas.getText(),
                    TxtJuamlah.getText().trim(),
                    TxtTaksiran.getText(),
                    TxtJarak.getText(),
                    TxtPerjalanan.getText(),
                    TxtArmada.getText(),
                    TxtKekurangan.getText(),
                    TxtPelapor.getText(),
                    TxtFile.getText()
            );
            crudObj.loadData(jTable1, "SELECT * FROM laporan_kebakaran");
        }
    }

