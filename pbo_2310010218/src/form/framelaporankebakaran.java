    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            String id = TxtID.getText();
            if (id.isEmpty()) {
                JOptionPane.showMessageDialog(null, "ID wajib diisi!");
                return;
            }
            
            String alamat = TxtAlamat.getText();
            String tgl = TxtWaktu.getText(); 
            String waktu = TxtLap.getText(); 
            String lap = jTextField5.getText(); 
            String lak = TxtLak.getText();
            String jenis = TxtJenis.getText();
            String luas = TxtLuas.getText();
            String jumlah = TxtJuamlah.getText().trim(); 
            String taksiran = TxtTaksiran.getText();
            String jarak = TxtJarak.getText();
            String perjalanan = TxtPerjalanan.getText();
            String armada = TxtArmada.getText();
            String kekuatan = TxtKekurangan.getText(); 
            String pelapor = TxtPelapor.getText();
            String file = TxtFile.getText();

            crudObj.simpanLaporanStatement(id, alamat, tgl, waktu, lap, lak, jenis, luas, jumlah, taksiran, jarak, perjalanan, armada, kekuatan, pelapor, file);

            if (crudObj.validasi) {
                TxtAlamat.setText(crudObj.var_alamat_lap);
                TxtWaktu.setText(crudObj.var_tgl_lap);
                TxtLap.setText(crudObj.var_waktu_lap);
                jTextField5.setText(crudObj.var_lap_lap);
                TxtLak.setText(crudObj.var_lak_lap);
                TxtJenis.setText(crudObj.var_jenis_lap);
                TxtLuas.setText(crudObj.var_luas_lap);
                TxtJuamlah.setText(crudObj.var_jumlah_lap);
                TxtTaksiran.setText(crudObj.var_taksiran_lap);
                TxtJarak.setText(crudObj.var_jarak_lap);
                TxtPerjalanan.setText(crudObj.var_perjalanan_lap);
                TxtArmada.setText(crudObj.var_armada_lap);
                TxtKekurangan.setText(crudObj.var_kekuatan_lap);
                TxtPelapor.setText(crudObj.var_pelapor_lap);
                TxtFile.setText(crudObj.var_file_lap);
            }
            crudObj.loadData(jTable1, "select * from laporan_kebakaran");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }

