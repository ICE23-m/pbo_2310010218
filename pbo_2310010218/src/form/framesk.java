    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            String ids = TxtID.getText();
            String nosk = TxtNosk.getText();
            String tglsk = TxtTanggalsk.getText();
            String nama = TxtNama.getText();
            String ket = TxtKeterangan.getText();
            String file = TxtFile.getText();

            if (ids.isEmpty() || nosk.isEmpty() || tglsk.isEmpty()) {
                JOptionPane.showMessageDialog(null, "ID, No SK, dan Tanggal SK wajib diisi!");
                return;
            }

            crudObj.simpanSKStatement(ids, nosk, tglsk, nama, ket, file);

            if (crudObj.validasi) {
                TxtNosk.setText(crudObj.var_nosk_sk);
                TxtTanggalsk.setText(crudObj.var_tglsk_sk);
                TxtNama.setText(crudObj.var_nama_sk);
                TxtKeterangan.setText(crudObj.var_ket_sk);
                TxtFile.setText(crudObj.var_file_sk);
            }
            crudObj.loadData(jTable1, "SELECT * FROM sk");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {
        if (
            TxtID.getText().isEmpty() ||
            TxtNosk.getText().isEmpty() ||
            TxtTanggalsk.getText().isEmpty()
           ){
            JOptionPane.showMessageDialog(null, "Pilih data dari tabel terlebih dahulu!");
            return;
        }

        crudObj.ubahSKPrepared(
                TxtID.getText(),
                TxtNosk.getText(),
                TxtTanggalsk.getText(),
                TxtNama.getText(),
                TxtKeterangan.getText(),
                TxtFile.getText()
        );
        crudObj.loadData(jTable1, "SELECT * FROM sk");
    }

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {
        if (TxtID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Pilih data dari tabel untuk dihapus!");
            return;
        }
        int konfirmasi = JOptionPane.showConfirmDialog(null, "Yakin ingin menghapus data ID: " + TxtID.getText() + "?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
        if (konfirmasi == JOptionPane.YES_OPTION) {
            
            crudObj.hapusSKPrepared(TxtID.getText());
            
            TxtID.setText(null);
            TxtNosk.setText(null);
            TxtTanggalsk.setText(null);
            TxtNama.setText(null);
            TxtKeterangan.setText(null);
            TxtFile.setText(null);
            
            crudObj.loadData(jTable1, "SELECT * FROM sk");
        }
    }

