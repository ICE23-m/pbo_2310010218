        if (baris == -1) {
            return;
        }
        TxtID.setText(jTable1.getValueAt(baris, 0).toString());
        TxtNosk.setText(jTable1.getValueAt(baris, 1).toString());
        TxtTanggalsk.setText(jTable1.getValueAt(baris, 2).toString());
        TxtNama.setText(jTable1.getValueAt(baris, 3) != null ? jTable1.getValueAt(baris, 3).toString() : "");
